#include "StdAfx.h"


//------------------------------------------------------------------------------------------------------------------
void Assert( bool pExpression, const char * pMsg )
{
	if( !pExpression )
	{
		Log( pMsg );
		__debugbreak();
	}
}


//------------------------------------------------------------------------------------------------------------------
void Log( const char * pFormat, ... )
{
	char szBuffer[1024];
 	va_list args;
	va_start( args, pFormat );
	vsprintf_s( szBuffer, pFormat, args );

	OutputDebugStringA( szBuffer );
}


